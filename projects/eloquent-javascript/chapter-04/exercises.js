// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
    //base
    let arr = array.length - 1;
    if (arr < 0) {
      return 0;
    }
    //recursion
    return array[arr] + sum(array.slice(0, arr));
};


