// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

const range = (x, y, inc) => {
  if(inc < 0) {
    return [];
  }
  if (x > y)
    return range(y,x).reverse();
  else  
    return x === y ? [] : [x, ...range(x + 1, y)];
}

