////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  if (x > y) {
    return y;
  } else {
    return x;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
  if (x % 2 === 0) {
    return true;
  } else { 
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  let count = 0;
  for (let i = 0; i < str.length; i++) {
    if( str[i].toUpperCase() === char.toUpperCase() )
    count ++;
  }
  return count
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  let count = 0;
  for (let i = 0; i < str.length; i++) {
    if( str[i].toUpperCase() === 'B' ){
      count ++;
    }
  }
  return count
}

