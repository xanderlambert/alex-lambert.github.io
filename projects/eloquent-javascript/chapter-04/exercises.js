const  range = (x, y, inc) => {
  let rangeArray=[];
  if ( x === y ){
    return [];
  }
  if(inc > 0){
    for(var i =x; i <= y; i += inc){
      rangeArray.push(i);
    }
  }
  else if(inc < 0) {
    for(var i = x; i >= y; i += inc){
      rangeArray.push(i);
    }
  }
  else {
     for(var i = x; i <= y; i++){
      rangeArray.push(i);
    }    
  }
  return rangeArray;
}


